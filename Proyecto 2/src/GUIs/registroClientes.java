    private void getRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getRoomActionPerformed
        try{
        String fName = name.getText().replace(" ", "");
        String nombre = fName.substring(0, 1).toUpperCase() + fName.substring(1);
        String lName = lastName.getText().replace(" ", "");
        String apellido = lName.substring(0, 1).toUpperCase() + lName.substring(1); 
        
        int room = hash.searchClient(nombre, apellido);
        if (room != -1){
            JOptionPane.showMessageDialog(null, "El cliente "+nombre+" "+apellido+" se encuentra hospedado en la habitacion NÂ°"+room+".");
        } else{
            if(use.containsNumbers(nombre) || use.containsNumbers(apellido)){
                JOptionPane.showMessageDialog(null, "ERROR - Por favor recuerde no ingresar numeros");
            }else{
            JOptionPane.showMessageDialog(null, "El cliente "+nombre+" "+apellido+" no se encuentra hospedado en el Hotel Oasis actualmente.");
        }
        }
        name.setText("");
        lastName.setText("");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "ERROR - Recuerde ingresar un nombre y un apellido");
        }
    }//GEN-LAST:event_getRoomActionPerformed

